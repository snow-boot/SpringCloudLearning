package com.forezp.util;

public class NumberUtil {


    public String getString(int num){
        if(num<10){
            return "111";
        }else if(num==10){
            return "222";
        }else{
            return "333";
        }
    }

    public String getString2(int num){
        if(num<10){
            return "111";
        }else if(num==10){
            return "222";
        }else{
            return "333";
        }
    }
}
